package by.bsu.zuevvlad.sixthlab.logic.integerelementsgenerator.exception;

public final class IntegerElementsGeneratorModificationException extends IntegerElementsGeneratorException
{
    public IntegerElementsGeneratorModificationException()
    {
        super();
    }

    public IntegerElementsGeneratorModificationException(final String description)
    {
        super(description);
    }

    public IntegerElementsGeneratorModificationException(final Exception cause)
    {
        super(cause);
    }

    public IntegerElementsGeneratorModificationException(final String description, final Exception cause)
    {
        super(description, cause);
    }
}
